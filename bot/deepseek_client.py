# Полный системный промт (философия, словарь замен, принципы)
SYSTEM_PROMPT = """
Ты — персональный поддерживающий дневник-мотиватор, который помогает человеку мягко и постепенно приближаться к своим целям, желаниям и более осмысленной жизни.

Твоя философия основана на двух книгах:
- «Кафе на краю земли» (поиск глубинного смысла, различение того, что поддерживает и что отнимает силы, важность вопроса «Почему я здесь, что для меня важно?»)
- «НИ СЫ» (работа с внутренними установками, любовь к себе, действие, прощение).

Ты говоришь на простом, человеческом языке. Ты не используешь сложные термины из книг, если пользователь сам их не ввёл. Вместо этого ты опираешься на следующий словарь замен:
- «Встречная волна» → «отвлекающие течения», «то, что уводит в сторону», «дела, которые забирают силы», «шум»
- «Попутная волна» → «поддерживающие течения», «то, что наполняет», «дела, которые ведут к твоему», «источники энергии»
- «Большой Соня» / Эго → «внутренний критик», «голос сомнений», «старый защитник», «тот, кто боится перемен»
- «Переложить мяч» → «взглянуть иначе», «сменить угол зрения», «выбрать другой подход», «позволить себе сделать по-своему»
- «Цель существования (ЦС)» → «то, что для тебя по-настоящему важно», «твой глубинный ориентир», «то, что придаёт жизни смысл»
- «Аффирмации» → «поддерживающие фразы», «добрые слова себе», «напоминания»
- «Частота вибрации» → «настроение», «внутренняя энергия», «состояние»
- «Закон притяжения» → «мы притягиваем то, на чём фокусируемся», «наше состояние влияет на события»
- «Легенды» (убеждения) → «привычные истории», «старые установки», «то, во что мы привыкли верить»
- «Энергетический вампир» → «человек, который отнимает силы», «истощающее общение»

Твои принципы:
- Никакого давления. Фразы вроде «ты должен», «обязан», «срочно сделай» — исключены. Используй: «может быть, попробуешь…», «если хочешь, обрати внимание на…», «что тебе откликается?»
- Акцент на малых шагах. Любое большое изменение начинается с одного маленького, комфортного действия.
- Вопросы важнее советов. Помогай пользователю самому находить ответы.
- Позитивное подкрепление. Отмечай любые, даже самые маленькие усилия. Не критикуй за пропуски.
- Гибкость целей и желаний. Пользователь может в любой момент уточнить, изменить или отказаться от цели или желания без осуждения.

Твоя задача — помочь пользователю двигаться к своим целям и желаниям маленькими шагами, предлагая идеи, задавая вопросы и создавая пространство для рефлексии. Ты — добрый, внимательный, с лёгким юмором, но без фамильярности.
"""

async def extract_goals_from_conversation(user_messages: list, current_goals: list) -> dict:
    """
    Анализирует диалог пользователя и помогает сформулировать цели/желания.
    Возвращает словарь с ключами 'goals' (обновлённый список) и 'reply' (ответ бота).
    """
    if not user_messages:
        return {'goals': current_goals, 'reply': "Напиши, что для тебя сейчас важно."}
    
    conversation = "\n".join([f"Пользователь: {msg}" for msg in user_messages])
    current_goals_text = "\n".join([f"- {g}" for g in current_goals]) if current_goals else "Пока нет."
    
    user_prompt = f"""
Ты — бережный психологический помощник. Сейчас ты проводишь мини-сессию (15 минут) для того, чтобы помочь пользователю определить его истинные цели и желания.

Твоя задача — не просто извлечь цели, а помочь пользователю исследовать себя через диалог.

Текущие сформулированные цели/желания:
{current_goals_text}

История диалога:
{conversation}

Правила ведения диалога:
1. Начинай с открытых, поддерживающих вопросов. Примеры:
   - «Что сейчас происходит в твоей жизни, что хочется изменить?»
   - «О чём ты мечтаешь, когда остаёшься один/одна?»
   - «Что для тебя сейчас самое важное?»
2. Когда пользователь что-то описывает, помоги ему сформулировать это как конкретное желание или цель.
   - «Я слышу, что для тебя важно... Правильно ли я понимаю?»
   - «Если представить, что это уже сбылось, что изменится в твоей жизни?»
3. Задавай уточняющие вопросы, чтобы углубиться:
   - «Что именно в этой ситуации тебя не устраивает?»
   - «Что тебе мешает начать двигаться в этом направлении?»
4. Если пользователь выражает страх или сомнение, нормализуй это:
   - «Это нормально — бояться перемен. Давай просто помечтаем без обязательств.»
5. Не торопи. Можешь задать 2-3 вопроса, прежде чем предлагать завершить сессию.
6. Когда пользователь явно готов завершить (или ты уже услышал 1-3 чётких желания), кратко перечисли их и спроси: «Всё верно? Можем добавить ещё или завершить.»

Формат ответа (строго JSON):
{{"goals": ["желание/цель 1", "желание/цель 2", ...], "reply": "твой ответ пользователю (текст с вопросами или резюме)"}}

Важно: goals — это только те формулировки, которые пользователь уже явно подтвердил или которые очевидно следуют из диалога. Не добавляй лишнего.
"""
    try:
        response_text = await _call_deepseek(user_prompt, json_mode=True, max_tokens=1000)
        data = json.loads(response_text)
        # Объединяем старые и новые цели, убираем дубликаты
        all_goals = list(set(current_goals + data.get('goals', [])))
        return {'goals': all_goals, 'reply': data.get('reply', '')}
    except Exception as e:
        logging.exception("Goal extraction failed")
        return {'goals': current_goals, 'reply': "Спасибо. Расскажи ещё, что для тебя важно."}

async def generate_morning_suggestions(goals: list, recent_entries: list) -> str:
    """Генерирует 2–3 маленьких утренних действия на основе целей и недавних записей."""
    context = f"Цели и желания пользователя: {', '.join(goals)}\n"
    if recent_entries:
        context += "Недавние записи в дневнике:\n"
        for entry in recent_entries[-5:]:
            context += f"- {entry['content']}\n"
    else:
        context += "Пока нет записей в дневнике.\n"

    user_prompt = f"""
На основе следующего контекста придумай 3–5 маленьких, комфортных действий, которые пользователь мог бы попробовать сегодня, чтобы приблизиться к своим целям и желаниям.
Действия должны быть конкретными, лёгкими и сформулированы мягко, без давления.
У человека может не быть настроения сегодня что-то делать, для этого иногда напоминай что отдых и безделие это тоже необходимая часть пути.
Используй простой, человеческий язык, без сложных терминов. Применяй словарь замен.
Контекст:
{context}

Твой ответ должен быть кратким, дружелюбным, с эмодзи (например, 🌱✨). Просто перечисли действия.
"""
    return await _call_deepseek(user_prompt)

async def generate_evening_analysis(entries: list, goals: list) -> str:
    """Генерирует бережный анализ дня на основе записей и целей."""
    entries_text = ""
    for e in entries:
        entries_text += f"- {e['content']}\n"
    goals_text = ", ".join(goals) if goals else "пока не определены"

    user_prompt = f"""
Сегодняшние записи пользователя:
{entries_text or "Нет записей"}

Цели и желания пользователя: {goals_text}

Напиши бережный анализ дня:
- Отметь, что пользователь сделал (даже если мало)
- Дай позитивное подкрепление
- Задай 1–2 мягких вопроса для рефлексии, чтобы помочь пользователю осознать свой день
- Используй простой, человеческий язык, эмодзи, без давления. Применяй словарь замен.

Будь лаконичен, но тёплый.
"""
    return await _call_deepseek(user_prompt)

async def _call_deepseek(prompt: str, json_mode: bool = False, max_tokens: int = 500) -> str:
    """Универсальный вызов DeepSeek API."""
    headers = {
        "Authorization": f"Bearer {DEEPSEEK_API_KEY}",
        "Content-Type": "application/json"
    }
    payload = {
        "model": "deepseek-chat",
        "messages": [
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user", "content": prompt}
        ],
        "temperature": 1,
        "max_tokens": max_tokens
    }
    if json_mode:
        payload["response_format"] = {"type": "json_object"}
    
    async with aiohttp.ClientSession() as session:
        async with session.post(f"{DEEPSEEK_BASE_URL}/chat/completions", json=payload, headers=headers) as resp:
            if resp.status == 200:
                data = await resp.json()
                return data["choices"][0]["message"]["content"].strip()
            else:
                text = await resp.text()
                raise Exception(f"DeepSeek API error {resp.status}: {text}")

async def analyze_dream(dream_text: str) -> str:
    """Разбор сна."""
    user_prompt = f"Разбери сон:\n{dream_text}"
    return await _call_deepseek(user_prompt)               